        function handleAspect(){
            var screenAspect = getScreenAspect();
            if(screenAspect < 1.298045602605863){
                $(".imageFrame").addClass("tallAspect");
            }
            else {
                $(".imageFrame").removeClass("tallAspect");
            }
        }
